# Definition for singly-linked list.
# class ListNode(object):
#     def __init__(self, val=0, next=None):
#         self.val = val
#         self.next = next
class Solution(object):
    def reverseBetween(self, head, left, right):
        """
        :type head: ListNode
        :type left: int
        :type right: int
        :rtype: ListNode
        """
        dummy = ListNode(0)
        dummy.next, pre, cnt = head, dummy, 1
        while pre and pre.next and cnt < left:
            pre, cnt = pre.next, cnt + 1
        tail = cur = pre.next
        while cur and cnt <= right:
            cur.next, pre.next, tail.next, cur, cnt = pre.next, cur, cur.next, cur.next, cnt + 1
        return dummy.next

