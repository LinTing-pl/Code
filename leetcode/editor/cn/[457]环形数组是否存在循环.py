class Solution(object):
    def circularArrayLoop(self, nums):
        """
        :type nums: List[int]
        :rtype: bool
        """
        n = len(nums)

        def next(cur):
            return (cur + nums[cur]) % n  # ä¿è¯è¿”å›å€¼åœ¨ [0,n) ä¸­

        for i, num in enumerate(nums):
            if num == 0:
                continue
            slow, fast = i, next(i)
            # åˆ¤æ–­éé›¶ä¸”æ–¹å‘ç›¸åŒ
            while nums[slow] * nums[fast] > 0 and nums[slow] * nums[next(fast)] > 0:
                if slow == fast:
                    if slow == next(slow):
                        break
                    return True
                slow = next(slow)
                fast = next(next(fast))
            add = i
            while nums[add] * nums[next(add)] > 0:
                tmp = add
                add = next(add)
                nums[tmp] = 0
        return False
