class Solution(object):
    def printTree(self, root):
        """
        :type root: TreeNode
        :rtype: List[List[str]]
        """
        # ç”±æ ‘çš„æ·±åº¦æ„å»ºäºŒç»´çŸ©é˜µï¼Œå¹¶dfsï¼Œåœ¨æ ¹èŠ‚ç‚¹åº”åœ¨ä½ç½®å¡«å……å€¼
        d = self.depth(root)
        C = [['' for _ in range(2 ** d - 1)] for _ in range(d)]

        def dfs(root, level=0, l=0, r=2 ** d - 1 - 1):
            if not root:
                return
            mid = (l + r) // 2
            C[level][mid] = str(root.val)
            dfs(root.left, level + 1, l, mid - 1)
            dfs(root.right, level + 1, mid + 1, r)

        dfs(root)
        return C

    def depth(self, root):
        if not root:
            return 0
        l = self.depth(root.left)
        r = self.depth(root.right)
        return max(l, r) + 1
